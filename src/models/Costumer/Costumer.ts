export class Costumer{
    id:string
    name:string
    lastname:string
    email:string
    phone:string
    constructor(costumerId:string,costumerName:string,costumerLastName:string,costumerEmail:string,costumerPhone:string){
        this.id=costumerId,
        this.name=costumerName,
        this.lastname=costumerLastName,
        this.email=costumerEmail,
        this.phone=costumerPhone}
}
